package com.tge.arithmeticoperator;

public class ArithmeticOperatorDemo5 {
    public static void main(String[] args) {
        int a = 10;
        a++;
        System.out.println(a);//11
        ++a;
        System.out.println(a);//12
        a--;
        System.out.println(a);//11
        --a;
        System.out.println(a);//10
    }
}
