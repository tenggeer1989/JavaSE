public class SwitchDemo2 {
    public static void main(String[] args) {
        int number = 100;
        switch (number){
            case 1:
                System.out.println("number is 1");
                break;
            case 10:
                System.out.println("number is 1");
                break;
            case 20:
                System.out.println("number is 1");
                break;
            /*default:
                System.out.println("number is 1");
                break;*/
        }
    }
}
